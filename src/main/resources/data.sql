CREATE TABLE STOCKS(ID INT AUTO_INCREMENT PRIMARY KEY, STOCK_CODE VARCHAR(10), STOCK_NAME VARCHAR(50), CREATED_DATE TIMESTAMP, UPDATED_DATE TIMESTAMP);
CREATE TABLE TRANSACTION (ID INT AUTO_INCREMENT PRIMARY KEY, TYPE TINYINT, STOCK_CODE VARCHAR(10), USER_ID INT, QUANTITY INT, PRICE INT, FEE NUMERIC(20, 2), CREATED_DATE TIMESTAMP,  UPDATED_DATE TIMESTAMP);
CREATE TABLE STOCKS_PRICE_HISTORY (ID INT AUTO_INCREMENT PRIMARY KEY, STOCK_CODE VARCHAR(10), YESTERDAY_CLOSE_PRICE INT, OPEN_PRICE INT, CLOSE_PRICE INT, HIGH_PRICE INT, LOW_PRICE INT, CURRENT_PRICE INT, TRANSACTION_VOLUME BIGINT, CREATED_DATE TIMESTAMP, UPDATED_DATE TIMESTAMP); -- 일별저장, 시가, 종가, 최고가 최저가는 각 조건에 따라 업데이트함
CREATE TABLE STOCKS_VIEW(ID INT AUTO_INCREMENT PRIMARY KEY, STOCK_CODE VARCHAR(10), VIEWS_COUNT BIGINT, CREATED_DATE DATE, UPDATED_DATE DATE);
CREATE TABLE COUNTRIES(ID INT AUTO_INCREMENT PRIMARY KEY, NAME VARCHAR(100), ISO_CODE VARCHAR(3), ISO_ALPHA_3 VARCHAR(3), ISO_ALPHA_2 VARCHAR(2), CREATED_DATE DATE, UPDATED_DATE DATE);
CREATE TABLE USERS(ID INT AUTO_INCREMENT PRIMARY KEY, NAME VARCHAR(50), EMAIL VARCHAR(100), CREATED_DATE TIMESTAMP, UPDATED_DATE TIMESTAMP);
CREATE TABLE USERS_WALLET(ID INT AUTO_INCREMENT PRIMARY KEY, USER_ID INT, STOCK_CODE VARCHAR(10), QUANTITY INT, CREATED_DATE TIMESTAMP, UPDATED_DATE TIMESTAMP);
CREATE TABLE shedlock (name VARCHAR(64), lock_until TIMESTAMP(3) NULL, locked_at TIMESTAMP(3) NULL, locked_by VARCHAR(255), PRIMARY KEY (name));

